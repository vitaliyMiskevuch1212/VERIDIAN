import React, { useState, useCallback, useEffect, useRef, createContext, useContext } from 'react';

// ─── Toast Context ───────────────────────────────────────────
// Creates a React context for toast state. Consumed via useToast()
// anywhere inside <ToastProvider>. Initialised to null so missing
// providers are caught early at runtime.
const ToastContext = createContext(null);

export function useToast() {
  // Convenience hook — components call useToast() instead of
  // importing both useContext and ToastContext separately.
  return useContext(ToastContext);
}

